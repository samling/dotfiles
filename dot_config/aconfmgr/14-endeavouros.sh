# endeavouros

AddPackage endeavouros-branding # Default EndeavourOS background images and icons
AddPackage endeavouros-keyring # EndeavourOS keyring
AddPackage endeavouros-mirrorlist # EndeavourOS mirrorlist
AddPackage eos-apps-info # Documentation about apps in the EndeavourOS repository.
AddPackage eos-dracut # Dracut scripts and hooks for EndeavourOS
AddPackage eos-hooks # EndeavourOS pacman hooks
AddPackage eos-log-tool # Gathers selected system logs and sends them to the internet.
AddPackage eos-packagelist # An application to gather and optionally install package lists from the EndeavourOS installer
AddPackage eos-quickstart # An application for getting a quick start with installing packages
AddPackage eos-rankmirrors # EndeavourOS mirror ranking tool
AddPackage rebuild-detector # Detects which packages need to be rebuilt
AddPackage reflector # A Python 3 module and script to retrieve and filter the latest Pacman mirror list.
AddPackage reflector-simple # Simple GUI wrapper for 'reflector'.
AddPackage welcome # Welcome greeter for new EndeavourOS users.
