# cli_tools

AddPackage bat # Cat clone with syntax highlighting and git integration
AddPackage direnv # A shell extension that manages your environment
AddPackage --foreign doppler-cli-bin # CLI utility for Doppler, environment and secrets manager.
AddPackage eza # A modern replacement for ls (community fork of exa)
AddPackage fx # Command-line tool and terminal JSON viewer
AddPackage glow # Command-line markdown renderer
AddPackage go-yq # Portable command-line YAML processor
AddPackage grc # Yet another colouriser for beautifying your logfiles or output of commands
AddPackage gron # Makes JSON greppable
AddPackage jc # Converts the output of popular command-line tools and file-types to JSON
AddPackage jq # Command-line JSON processor
AddPackage lsd # Modern ls with a lot of pretty colors and awesome icons
AddPackage --foreign pet-git # Simple command-line snippet manager, written in Go.
AddPackage tree # A directory listing program displaying a depth indented list of files
AddPackage zoxide # A smarter cd command for your terminal
